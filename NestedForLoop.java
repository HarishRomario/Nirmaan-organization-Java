package day7;

import java.util.Scanner;

public class NestedForLoop {

	public static void main(String[] args) {
		Scanner sc = new Scanner (System.in);
		System.out.println("Enter the Row: ");
		int a = sc.nextInt();
		System.out.println("Enter the Coloumn: ");
		int b = sc.nextInt();
		for(int i=1;i<=a;i++) {
			System.out.print(" ");
			for(int j=1;j<=b;j++) {
				System.out.println(" # ");
				
				}
			}
			
		}
		

	}

